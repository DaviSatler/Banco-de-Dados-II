a = float(input('Diga o valor do primeiro número: '))
b = float(input('Diga o valor do segundo número: '))

soma = a + b
sub = a - b
multi = a * b
div = a / b

print(f'A soma desses valores é igual a {soma}')
print(f'A subtração desses valores é igual a {sub}')
print(f'A multiplicação desses valores é igual a {multi}')
print(f'A divisão desses valores é igual a {div}')
