# 7 - Dado a lista: precos = [10.5, 22.0, 12.5, 22.0, 20.5], mostre a quantidade de elementos e o preço médio dos produtos.

precos = [10.5, 22.0, 12.5, 22.0, 20.5]

quantidade = len(precos)
media = sum(precos) / quantidade

print(f"Quantidade de elementos: {quantidade}")
print(f"Preço médio: {media} \n")